// interpolate the two poses into s_lerped[]; shellscale extrudes the mesh
// along the vertex normals for RF_SHELL_* passes (GL_LerpVerts)
static void GL3_LerpAliasFrame (entity_t *e, dmdl_t *paliashdr, float backlerp, float shellscale)
{
	daliasframe_t	*frame, *oldframe;
	dtrivertx_t		*v, *ov;
	float			frontlerp;
	vec3_t			move, delta, vectors[3];
	vec3_t			frontv, backv;
	int				i;

	frame = (daliasframe_t *)((byte *)paliashdr + paliashdr->ofs_frames
		+ e->frame * paliashdr->framesize);
	v = frame->verts;

	oldframe = (daliasframe_t *)((byte *)paliashdr + paliashdr->ofs_frames
		+ e->oldframe * paliashdr->framesize);
	ov = oldframe->verts;

	frontlerp = 1.0f - backlerp;

	// move should be the delta back to the previous frame * backlerp
	VectorSubtract (e->oldorigin, e->origin, delta);
	AngleVectors (e->angles, vectors[0], vectors[1], vectors[2]);

	move[0] = DotProduct (delta, vectors[0]);	// forward
	move[1] = -DotProduct (delta, vectors[1]);	// left
	move[2] = DotProduct (delta, vectors[2]);	// up

	VectorAdd (move, oldframe->translate, move);

	for (i = 0; i < 3; i++)
		move[i] = backlerp * move[i] + frontlerp * frame->translate[i];

	for (i = 0; i < 3; i++)
	{
		frontv[i] = frontlerp * frame->scale[i];
		backv[i] = backlerp * oldframe->scale[i];
	}

	for (i = 0; i < paliashdr->num_xyz; i++, v++, ov++)
	{
		s_lerped[i][0] = move[0] + ov->v[0] * backv[0] + v->v[0] * frontv[0];
		s_lerped[i][1] = move[1] + ov->v[1] * backv[1] + v->v[1] * frontv[1];
		s_lerped[i][2] = move[2] + ov->v[2] * backv[2] + v->v[2] * frontv[2];
		if (shellscale)
		{
			float	*n = r_avertexnormals[v->lightnormalindex];
			s_lerped[i][0] += n[0] * shellscale;
			s_lerped[i][1] += n[1] * shellscale;
			s_lerped[i][2] += n[2] * shellscale;
		}
	}
}

void GL3_DrawAliasModel (entity_t *e, const float *viewproj)
{
	dmdl_t			*paliashdr;
	daliasframe_t	*frame;
	dtrivertx_t		*verts;
	image_t			*skin;
	int				*order;
	int				count;
	float			alpha;
	float			model[16], mvp[16], tmp[16];
	int				i;

	if ((e->flags & RF_WEAPONMODEL) && r_lefthand->value == 2.0f)
		return;			// hand 2: no view weapon at all

	paliashdr = (dmdl_t *)e->model->extradata;
	c_alias_polys += paliashdr->num_tris;

	// validate frame indices
	if ( (e->frame >= paliashdr->num_frames) || (e->frame < 0) )
	{
		ri.Con_Printf (PRINT_ALL, "GL3_DrawAliasModel %s: no such frame %d\n",
			e->model->name, e->frame);
		e->frame = 0;
		e->oldframe = 0;
	}
	if ( (e->oldframe >= paliashdr->num_frames) || (e->oldframe < 0) )
	{
		ri.Con_Printf (PRINT_ALL, "GL3_DrawAliasModel %s: no such oldframe %d\n",
			e->model->name, e->oldframe);
		e->frame = 0;
		e->oldframe = 0;
	}

	int	shell = e->flags & (RF_SHELL_RED | RF_SHELL_GREEN | RF_SHELL_BLUE
			| RF_SHELL_DOUBLE | RF_SHELL_HALF_DAM);

	//
	// lighting (PMM's shell cascade, ported exactly from R_DrawAliasModel)
	//
	if (shell)
	{
		VectorClear (shadelight);
		if ((e->flags & RF_SHELL_RED) && (e->flags & RF_SHELL_BLUE)
			&& (e->flags & RF_SHELL_GREEN))
		{	// special case for godmode
			shadelight[0] = shadelight[1] = shadelight[2] = 1.0f;
		}
		else if (e->flags & (RF_SHELL_RED | RF_SHELL_BLUE | RF_SHELL_DOUBLE))
		{
			if (e->flags & RF_SHELL_RED)
			{
				shadelight[0] = 1.0f;
				if (e->flags & (RF_SHELL_BLUE | RF_SHELL_DOUBLE))
					shadelight[2] = 1.0f;
			}
			else if (e->flags & RF_SHELL_BLUE)
			{
				if (e->flags & RF_SHELL_DOUBLE)
				{
					shadelight[1] = 1.0f;
					shadelight[2] = 1.0f;
				}
				else
					shadelight[2] = 1.0f;
			}
			else if (e->flags & RF_SHELL_DOUBLE)
			{
				shadelight[0] = 0.9f;
				shadelight[1] = 0.7f;
			}
		}
		else if (e->flags & (RF_SHELL_HALF_DAM | RF_SHELL_GREEN))
		{
			if (e->flags & RF_SHELL_HALF_DAM)
			{
				shadelight[0] = 0.56f; shadelight[1] = 0.59f; shadelight[2] = 0.45f;
			}
			if (e->flags & RF_SHELL_GREEN)
				shadelight[1] = 1.0f;
		}
	}
	else if (e->flags & RF_FULLBRIGHT)
	{
		shadelight[0] = shadelight[1] = shadelight[2] = 1.0f;
	}
	else
	{
		R_LightPoint (e->origin, shadelight);
	}

	if (e->flags & RF_MINLIGHT)
	{	// keep pickups visible in the dark (id applies this to shells too)
		for (i = 0; i < 3; i++)
			if (shadelight[i] > 0.1f)
				break;
		if (i == 3)
			shadelight[0] = shadelight[1] = shadelight[2] = 0.1f;
	}

	if (e->flags & RF_GLOW)
	{	// bonus items pulse
		float	scale = 0.1f * (float)sin (r_newrefdef.time * 7);
		for (i = 0; i < 3; i++)
		{
			float	min = shadelight[i] * 0.8f;
			shadelight[i] += scale;
			if (shadelight[i] < min)
				shadelight[i] = min;
		}
	}

	if (e->flags & RF_CHARRED)
	{	// burned to death: the same skin, crushed to sooty black with a
		// faint warm remnant so the silhouette still reads in the dark
		shadelight[0] = shadelight[0] * 0.16f + 0.02f;
		shadelight[1] = shadelight[1] * 0.12f + 0.012f;
		shadelight[2] = shadelight[2] * 0.10f + 0.008f;
	}

	// PGM: ir goggles color override (Rogue)
	if ((r_newrefdef.rdflags & RDF_IRGOGGLES) && (e->flags & RF_IR_VISIBLE))
	{
		shadelight[0] = 1.0f;
		shadelight[1] = 0.0f;
		shadelight[2] = 0.0f;
	}

	shadedots = r_avertexnormal_dots[
		((int)(e->angles[1] * (SHADEDOT_QUANT / 360.0))) & (SHADEDOT_QUANT - 1)];

	//
	// build the model matrix (matches R_RotateForEntity with the original's
	// alias-only pitch negation -- "e->angles[PITCH] = -e->angles[PITCH]; sigh."
	// -- so the net pitch rotation is positive; without it the view weapon
	// pitches opposite to the camera)
	//
	GL3_MatIdentity (model);
	GL3_MatTranslate (tmp, e->origin[0], e->origin[1], e->origin[2]);
	GL3_MatMul (model, model, tmp);
	GL3_MatRotate (tmp, e->angles[1], 0, 0, 1);		// yaw
	GL3_MatMul (model, model, tmp);
	GL3_MatRotate (tmp, e->angles[0], 0, 1, 0);		// pitch (sign flipped, see above)
	GL3_MatMul (model, model, tmp);
	GL3_MatRotate (tmp, -e->angles[2], 1, 0, 0);	// roll
	GL3_MatMul (model, model, tmp);

	GL3_MatMul (mvp, viewproj, model);

	// hand 1: mirror the view weapon by flipping clip-space X (id scales the
	// projection by -1,1,1); the mirrored winding needs the opposite cull face
	int	lefthand = (e->flags & RF_WEAPONMODEL) && r_lefthand->value == 1.0f;
	if (lefthand)
	{
		mvp[0] = -mvp[0]; mvp[4] = -mvp[4]; mvp[8] = -mvp[8]; mvp[12] = -mvp[12];
		glCullFace (GL_BACK);
	}

	//
	// interpolate vertices
	//
	GL3_LerpAliasFrame (e, paliashdr, e->backlerp, shell ? POWERSUIT_SCALE : 0.0f);

	// current-frame verts supply the lighting normal indices
	frame = (daliasframe_t *)((byte *)paliashdr + paliashdr->ofs_frames
		+ e->frame * paliashdr->framesize);
	verts = frame->verts;

	if (shell)
		alpha = 0.30f;			// shells always blend at 0.30 (id)
	else if (e->flags & RF_TRANSLUCENT)
		alpha = e->alpha;
	else
		alpha = 1.0f;

	//
	// walk the glcmds, expanding each strip/fan run into an independent
	// triangle list so it can be drawn with a single GL_TRIANGLES call
	//
	order = (int *)((byte *)paliashdr + paliashdr->ofs_glcmds);
	s_numverts = 0;

	while (1)
	{
		qboolean	fan;

		count = *order++;
		if (!count)
			break;		// done

		if (count < 0)
		{
			count = -count;
			fan = true;
		}
		else
		{
			fan = false;
		}

		if (count > MAX_ALIAS_VERTS)
			count = MAX_ALIAS_VERTS;	// safety clamp

		// gather the run's vertices
		for (i = 0; i < count; i++)
		{
			gl3_meshvert_t	*rv = &s_runbuf[i];
			float			s = ((float *)order)[0];
			float			t = ((float *)order)[1];
			int				index_xyz = order[2];
			float			l;

			// a malicious model can put an out-of-range vertex index in its
			// glcmds; clamp before it indexes verts[]/s_lerped[]
			if (index_xyz < 0 || index_xyz >= paliashdr->num_xyz)
				index_xyz = 0;

			l = shell ? 1.0f : shadedots[verts[index_xyz].lightnormalindex];

			order += 3;

			VectorCopy (s_lerped[index_xyz], rv->pos);
			rv->st[0] = s;
			rv->st[1] = t;
			// id's fixed-function glColor clamps at 1.0; without the clamp
			// lit sides blow out (shadedots peaks at ~2)
			rv->color[0] = l * shadelight[0] > 1.0f ? 1.0f : l * shadelight[0];
			rv->color[1] = l * shadelight[1] > 1.0f ? 1.0f : l * shadelight[1];
			rv->color[2] = l * shadelight[2] > 1.0f ? 1.0f : l * shadelight[2];
			rv->color[3] = alpha;
		}

		// expand to triangles, preserving GL strip/fan winding
		if (fan)
		{
			for (i = 2; i < count; i++)
			{
				GL3_EmitVert (&s_runbuf[0]);
				GL3_EmitVert (&s_runbuf[i - 1]);
				GL3_EmitVert (&s_runbuf[i]);
			}
		}
		else
		{
			for (i = 2; i < count; i++)
			{
				if (i & 1)
				{
					// odd triangle: swap first two to keep winding
					GL3_EmitVert (&s_runbuf[i - 1]);
					GL3_EmitVert (&s_runbuf[i - 2]);
					GL3_EmitVert (&s_runbuf[i]);
				}
				else
				{
					GL3_EmitVert (&s_runbuf[i - 2]);
					GL3_EmitVert (&s_runbuf[i - 1]);
					GL3_EmitVert (&s_runbuf[i]);
				}
			}
		}
	}

	//
	// select skin
	//
	if (e->skin)
	{
		skin = e->skin;					// custom player skin
	}
	else
	{
		int	sn = e->skinnum;

		if (sn < 0 || sn >= MAX_MD2SKINS)
			sn = 0;
		skin = e->model->skins[sn];
		if (!skin)
			skin = e->model->skins[0];
	}
	if (!skin)
		skin = r_notexture;				// fallback

	if (shell)
		GL3_Bind (white_tex);			// untextured flat-colour shell
	else
		GL3_Bind (skin->texnum);

	//
	// draw
	//
	glUseProgram (gl3_prog_alias.program);
	glUniformMatrix4fv (gl3_prog_alias.u_mvp, 1, GL_FALSE, mvp);
	glUniform1f (gl3_prog_alias.u_gamma,
		vid_gamma->value < 0.5f ? 0.5f : vid_gamma->value);
	// shells are untextured flat colour in id -- intensity would wash the
	// hue out (DOUBLE's orange to yellow), so only skins get the boost
	glUniform1f (gl3_prog_alias.u_intensity, shell ? 1.0f : gl_intensity->value);

	if (shell || (e->flags & RF_TRANSLUCENT))
	{
		glEnable (GL_BLEND);
		glBlendFunc (GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	}

	// view weapon: compress depth range so it never pokes into world geometry
	if (e->flags & RF_DEPTHHACK)
		glDepthRange (0.0, 0.3);

	glBindVertexArray (mesh_vao);
	glBindBuffer (GL_ARRAY_BUFFER, mesh_vbo);
	glBufferData (GL_ARRAY_BUFFER, s_numverts * sizeof(gl3_meshvert_t),
		s_meshverts, GL_STREAM_DRAW);
	glDrawArrays (GL_TRIANGLES, 0, s_numverts);
	glBindVertexArray (0);
	glBindBuffer (GL_ARRAY_BUFFER, 0);

	if (e->flags & RF_DEPTHHACK)
		glDepthRange (0.0, 1.0);
	if (lefthand)
		glCullFace (GL_FRONT);

	if (shell || (e->flags & RF_TRANSLUCENT))
		glDisable (GL_BLEND);

	// soft blob shadow: only entities whose lighting traced the ground
	// this call (non-shell, non-fullbright), and never the view weapon
	if (gl_shadows && gl_shadows->value && !shell
		&& !(e->flags & (RF_FULLBRIGHT | RF_WEAPONMODEL | RF_TRANSLUCENT | RF_DEPTHHACK | RF_BEAM)))
		GL3_DrawBlobShadow (e, viewproj);
}

/*
=================
GL3_DrawBeam

RF_BEAM entities (grapple cable, lightning): a segmented cylinder from
origin to old_origin, width = e->frame, palette colour = e->skinnum
(R_DrawBeam).
=================
*/
#define NUM_BEAM_SEGS 6

void GL3_DrawBeam (entity_t *e, const float *viewproj)
{
	int		i, j;
	vec3_t	perpvec, direction, normalized_direction;
	vec3_t	start_points[NUM_BEAM_SEGS], end_points[NUM_BEAM_SEGS];
	byte	*rgba;
	gl3_meshvert_t	v;

	VectorSubtract (e->oldorigin, e->origin, direction);
	VectorCopy (direction, normalized_direction);
	if (VectorNormalize (normalized_direction) == 0)
		return;

	PerpendicularVector (perpvec, normalized_direction);
	VectorScale (perpvec, e->frame / 2, perpvec);

	for (i = 0; i < NUM_BEAM_SEGS; i++)
	{
		RotatePointAroundVector (start_points[i], normalized_direction, perpvec,
			(360.0f / NUM_BEAM_SEGS) * i);
		VectorAdd (start_points[i], e->origin, start_points[i]);
		VectorAdd (start_points[i], direction, end_points[i]);
	}

	rgba = (byte *)&d_8to24table[e->skinnum & 0xFF];
	v.st[0] = v.st[1] = 0;
	v.color[0] = rgba[0] / 255.0f;
	v.color[1] = rgba[1] / 255.0f;
	v.color[2] = rgba[2] / 255.0f;
	v.color[3] = 0.30f;

	s_numverts = 0;
	for (i = 0; i < NUM_BEAM_SEGS; i++)
	{	// quad between ring i and ring i+1, as two triangles
		j = (i + 1) % NUM_BEAM_SEGS;
		VectorCopy (start_points[i], v.pos); GL3_EmitVert (&v);
		VectorCopy (end_points[i], v.pos);   GL3_EmitVert (&v);
		VectorCopy (start_points[j], v.pos); GL3_EmitVert (&v);
		VectorCopy (start_points[j], v.pos); GL3_EmitVert (&v);
		VectorCopy (end_points[i], v.pos);   GL3_EmitVert (&v);
		VectorCopy (end_points[j], v.pos);   GL3_EmitVert (&v);
	}

	glUseProgram (gl3_prog_alias.program);
	glUniformMatrix4fv (gl3_prog_alias.u_mvp, 1, GL_FALSE, viewproj);	// world space
	glUniform1f (gl3_prog_alias.u_gamma,
		vid_gamma->value < 0.5f ? 0.5f : vid_gamma->value);
	glUniform1f (gl3_prog_alias.u_intensity, 1.0f);

	GL3_Bind (white_tex);
	glEnable (GL_BLEND);
	glBlendFunc (GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	glDepthMask (GL_FALSE);
	glDisable (GL_CULL_FACE);

	glBindVertexArray (mesh_vao);
	glBindBuffer (GL_ARRAY_BUFFER, mesh_vbo);
	glBufferData (GL_ARRAY_BUFFER, s_numverts * sizeof(gl3_meshvert_t),
		s_meshverts, GL_STREAM_DRAW);
	glDrawArrays (GL_TRIANGLES, 0, s_numverts);
	glBindVertexArray (0);
	glBindBuffer (GL_ARRAY_BUFFER, 0);

	glEnable (GL_CULL_FACE);
	glDepthMask (GL_TRUE);
	glDisable (GL_BLEND);
}

/*
=================
GL3_DrawNullModel

Entities whose model failed to load draw as a small lit diamond
(R_DrawNullModel), so missing assets are visible instead of silent.
=================
*/
void GL3_DrawNullModel (entity_t *e, const float *viewproj)
{
	vec3_t	light, rim[4];
	float	model[16], mvp[16], tmp[16];
	float	alpha = (e->flags & RF_TRANSLUCENT) ? e->alpha : 1.0f;
	int		i, j;

	if (e->flags & RF_FULLBRIGHT)
		light[0] = light[1] = light[2] = 1.0f;
	else
		R_LightPoint (e->origin, light);

	// R_RotateForEntity, yaw only -- the diamond is rotationally symmetric
	GL3_MatTranslate (model, e->origin[0], e->origin[1], e->origin[2]);
	GL3_MatRotate (tmp, e->angles[1], 0, 0, 1);
	GL3_MatMul (model, model, tmp);
	GL3_MatMul (mvp, viewproj, model);

	// two 4-segment cones sharing a square rim at z=0
	for (i = 0; i < 4; i++)
	{
		rim[i][0] = 16.0f * cosf (i * (float)M_PI / 2.0f);
		rim[i][1] = 16.0f * sinf (i * (float)M_PI / 2.0f);
		rim[i][2] = 0.0f;
	}

	s_numverts = 0;
	for (i = 0; i < 4; i++)
	{
		gl3_meshvert_t	v;

		v.st[0] = v.st[1] = 0.0f;
		v.color[0] = light[0]; v.color[1] = light[1]; v.color[2] = light[2];
		v.color[3] = alpha;

		j = (i + 1) & 3;
		VectorSet (v.pos, 0, 0, -16);    GL3_EmitVert (&v);
		VectorCopy (rim[i], v.pos);      GL3_EmitVert (&v);
		VectorCopy (rim[j], v.pos);      GL3_EmitVert (&v);
		VectorSet (v.pos, 0, 0, 16);     GL3_EmitVert (&v);
		VectorCopy (rim[j], v.pos);      GL3_EmitVert (&v);
		VectorCopy (rim[i], v.pos);      GL3_EmitVert (&v);
	}

	glUseProgram (gl3_prog_alias.program);
	glUniformMatrix4fv (gl3_prog_alias.u_mvp, 1, GL_FALSE, mvp);
	glUniform1f (gl3_prog_alias.u_intensity, 1.0f);
	glUniform1f (gl3_prog_alias.u_alphacut, 0.0f);
	GL3_Bind (white_tex);

	if (alpha < 1.0f)
	{
		glEnable (GL_BLEND);
		glBlendFunc (GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	}
	glDisable (GL_CULL_FACE);

	glBindVertexArray (mesh_vao);
	glBindBuffer (GL_ARRAY_BUFFER, mesh_vbo);
	glBufferData (GL_ARRAY_BUFFER, s_numverts * sizeof(gl3_meshvert_t),
		s_meshverts, GL_STREAM_DRAW);
	glDrawArrays (GL_TRIANGLES, 0, s_numverts);
	glBindVertexArray (0);
	glBindBuffer (GL_ARRAY_BUFFER, 0);

	glEnable (GL_CULL_FACE);
	if (alpha < 1.0f)
		glDisable (GL_BLEND);
}

/*
=================
GL3_RenderDlights

gl_flashblend: instead of rebuilding lightmaps, draw each dynamic light as
an additive-blended ball -- colored center fading to black at the rim, so
the add falls off smoothly (R_RenderDlights/R_RenderDlight).
=================
*/
#define DLIGHT_BALL_SEGS 16

void GL3_RenderDlights (const float *viewproj)
{
	int			i, j, k;
	dlight_t	*dl;
	vec3_t		vpn, vright, vup;

	if (!gl_flashblend || !gl_flashblend->value)
		return;
	if (!r_newrefdef.num_dlights)
		return;

	AngleVectors (r_newrefdef.viewangles, vpn, vright, vup);

	s_numverts = 0;
	dl = r_newrefdef.dlights;
	for (i = 0; i < r_newrefdef.num_dlights; i++, dl++)
	{
		gl3_meshvert_t	center, edge;
		float			rad = dl->intensity * 0.35f;

		// center pulled toward the viewer, colored; rim on the view plane,
		// black (adds nothing)
		for (j = 0; j < 3; j++)
			center.pos[j] = dl->origin[j] - vpn[j] * rad;
		center.st[0] = center.st[1] = 0.0f;
		center.color[0] = dl->color[0] * 0.2f;
		center.color[1] = dl->color[1] * 0.2f;
		center.color[2] = dl->color[2] * 0.2f;
		center.color[3] = 1.0f;

		edge = center;
		edge.color[0] = edge.color[1] = edge.color[2] = 0.0f;

		for (k = 0; k < DLIGHT_BALL_SEGS; k++)
		{
			float	a0 = k * (2.0f * (float)M_PI / DLIGHT_BALL_SEGS);
			float	a1 = (k + 1) * (2.0f * (float)M_PI / DLIGHT_BALL_SEGS);

			GL3_EmitVert (&center);
			for (j = 0; j < 3; j++)
				edge.pos[j] = dl->origin[j] + vright[j] * cosf (a0) * rad
					+ vup[j] * sinf (a0) * rad;
			GL3_EmitVert (&edge);
			for (j = 0; j < 3; j++)
				edge.pos[j] = dl->origin[j] + vright[j] * cosf (a1) * rad
					+ vup[j] * sinf (a1) * rad;
			GL3_EmitVert (&edge);
		}
	}

	glUseProgram (gl3_prog_alias.program);
	glUniformMatrix4fv (gl3_prog_alias.u_mvp, 1, GL_FALSE, viewproj);	// world space
	glUniform1f (gl3_prog_alias.u_intensity, 1.0f);
	glUniform1f (gl3_prog_alias.u_alphacut, 0.0f);
	GL3_Bind (white_tex);

	glDepthMask (GL_FALSE);
	glEnable (GL_BLEND);
	glBlendFunc (GL_ONE, GL_ONE);		// additive glow
	glDisable (GL_CULL_FACE);

	glBindVertexArray (mesh_vao);
	glBindBuffer (GL_ARRAY_BUFFER, mesh_vbo);
	glBufferData (GL_ARRAY_BUFFER, s_numverts * sizeof(gl3_meshvert_t),
		s_meshverts, GL_STREAM_DRAW);
	glDrawArrays (GL_TRIANGLES, 0, s_numverts);
	glBindVertexArray (0);
	glBindBuffer (GL_ARRAY_BUFFER, 0);

	glEnable (GL_CULL_FACE);
	glBlendFunc (GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	glDisable (GL_BLEND);
	glDepthMask (GL_TRUE);
}

/*
=================
GL3_DrawSpriteModel

SP2 sprites are camera-facing quads in world space, offset by the frame's
raster origin (matches R_DrawSpriteModel). Opaque sprites use the classic
alpha-test cutoff (0.666, via shader discard); translucent ones blend.
=================
*/
void GL3_DrawSpriteModel (entity_t *e, const float *viewproj)
{
	dsprite_t		*psprite = (dsprite_t *)e->model->extradata;
	dsprframe_t		*frame;
	image_t			*skin;
	vec3_t			vpn, vright, vup;
	vec3_t			light = { 1, 1, 1 };
	float			alpha = 1.0f;
	int				framenum, i;
	gl3_meshvert_t	v[4];
	static const float corner_st[4][2] = { {0, 1}, {0, 0}, {1, 0}, {1, 1} };

	framenum = e->frame % psprite->numframes;
	frame = &psprite->frames[framenum];

	skin = e->model->skins[framenum];
	if (!skin)
		skin = r_notexture;

	if (e->flags & RF_TRANSLUCENT)
		alpha = e->alpha;

	// RF_MINLIGHT on a sprite = sit it in the scene's light (corpse flies).
	// Everything else keeps id's unlit white -- the BFG ball and explosion
	// sprites must glow in the dark, and id never lit sprites at all.
	if ((e->flags & RF_MINLIGHT) && !(e->flags & RF_FULLBRIGHT)
		&& !(r_newrefdef.rdflags & RDF_NOWORLDMODEL))
	{
		R_LightPoint (e->origin, light);
		for (i = 0; i < 3; i++)
		{
			if (light[i] < 0.10f)	light[i] = 0.10f;	// the MINLIGHT part
			if (light[i] > 1.0f)	light[i] = 1.0f;
		}
	}

	// billboard axes from the view angles
	AngleVectors (r_newrefdef.viewangles, vpn, vright, vup);

	// corners: (0,1) bottom-left, (0,0) top-left, (1,0) top-right, (1,1) bottom-right
	VectorMA (e->origin, -frame->origin_y, vup, v[0].pos);
	VectorMA (v[0].pos, -frame->origin_x, vright, v[0].pos);
	VectorMA (e->origin, frame->height - frame->origin_y, vup, v[1].pos);
	VectorMA (v[1].pos, -frame->origin_x, vright, v[1].pos);
	VectorMA (e->origin, frame->height - frame->origin_y, vup, v[2].pos);
	VectorMA (v[2].pos, frame->width - frame->origin_x, vright, v[2].pos);
	VectorMA (e->origin, -frame->origin_y, vup, v[3].pos);
	VectorMA (v[3].pos, frame->width - frame->origin_x, vright, v[3].pos);

	for (i = 0; i < 4; i++)
	{
		v[i].st[0] = corner_st[i][0];
		v[i].st[1] = corner_st[i][1];
		v[i].color[0] = light[0];
		v[i].color[1] = light[1];
		v[i].color[2] = light[2];
		v[i].color[3] = alpha;
	}

	// two triangles preserving the quad's order
	s_numverts = 0;
	GL3_EmitVert (&v[0]); GL3_EmitVert (&v[1]); GL3_EmitVert (&v[2]);
	GL3_EmitVert (&v[0]); GL3_EmitVert (&v[2]); GL3_EmitVert (&v[3]);

	GL3_Bind (skin->texnum);

	glUseProgram (gl3_prog_alias.program);
	glUniformMatrix4fv (gl3_prog_alias.u_mvp, 1, GL_FALSE, viewproj);	// verts are world-space
	glUniform1f (gl3_prog_alias.u_gamma,
		vid_gamma->value < 0.5f ? 0.5f : vid_gamma->value);
	glUniform1f (gl3_prog_alias.u_intensity, gl_intensity->value);

	if (alpha < 1.0f)
	{
		glEnable (GL_BLEND);
		glBlendFunc (GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	}
	else
	{
		glUniform1f (gl3_prog_alias.u_alphacut, 0.666f);
	}

	glDisable (GL_CULL_FACE);		// billboards have no meaningful backface

	glBindVertexArray (mesh_vao);
	glBindBuffer (GL_ARRAY_BUFFER, mesh_vbo);
	glBufferData (GL_ARRAY_BUFFER, s_numverts * sizeof(gl3_meshvert_t),
		s_meshverts, GL_STREAM_DRAW);
	glDrawArrays (GL_TRIANGLES, 0, s_numverts);
	glBindVertexArray (0);
	glBindBuffer (GL_ARRAY_BUFFER, 0);

	glEnable (GL_CULL_FACE);
	if (alpha < 1.0f)
		glDisable (GL_BLEND);
	else
		glUniform1f (gl3_prog_alias.u_alphacut, 0.0f);
}
